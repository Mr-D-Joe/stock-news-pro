import { Component, type ErrorInfo, type ReactNode } from 'react';

interface Props {
    children: ReactNode;
}

interface State {
    hasError: boolean;
    error: Error | null;
    errorInfo: ErrorInfo | null;
}

export class ErrorBoundary extends Component<Props, State> {
    public state: State = {
        hasError: false,
        error: null,
        errorInfo: null
    };

    public static getDerivedStateFromError(error: Error): State {
        return { hasError: true, error, errorInfo: null };
    }

    public componentDidCatch(error: Error, errorInfo: ErrorInfo) {
        console.error("Uncaught error:", error, errorInfo);
        this.setState({ errorInfo });
    }

    public render() {
        if (this.state.hasError) {
            return (
                <div className="p-8 bg-red-50 min-h-screen text-red-900 font-mono">
                    <h1 className="text-2xl font-bold mb-4">Software Crash Detected</h1>
                    <p className="mb-4">Something went wrong in the component tree.</p>
                    <div className="bg-white p-4 rounded border border-red-200 overflow-auto whitespace-pre-wrap">
                        <strong className="block mb-2">{this.state.error?.toString()}</strong>
                        <span className="text-sm opacity-75">
                            {this.state.errorInfo?.componentStack}
                        </span>
                    </div>
                </div>
            );
        }

        return this.props.children;
    }
}
